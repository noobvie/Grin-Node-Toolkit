#!/bin/bash
# =============================================================================
# 085_ssh_hardening.sh - SSH Key Hardening (root login by key, not password)
# =============================================================================
# Gets a working SSH public key onto the server, verifies key login, then
# optionally disables password authentication so the box is reachable by key
# only — with a guaranteed rollback / break-glass path so you can never lock
# yourself out.
#
# Scope: root account only (the toolkit already runs as root on the VPS).
#
# Key handling (3 methods, in order):
#   1) Paste manually          — paste the public key from your laptop
#   2) Generate on server      — ssh-keygen here; private key shown ONCE to save
#   3) Fetch from URL          — github username, or any raw URL serving a
#                                PUBLIC key (e.g. github.com/<user>.keys, or a
#                                self-hosted pubkey directory). Private keys are
#                                NEVER generated by or fetched from a website.
#
# Hardening is staged safely:
#   · changes go to a drop-in (/etc/ssh/sshd_config.d/99-grin-hardening.conf)
#     when the distro supports Include, else a marked block in sshd_config
#   · every change is validated with `sshd -t` BEFORE it is applied
#   · sshd is RELOADED, never restarted (your current session survives)
#   · disabling passwords is gated behind an authorized_keys check + an
#     optional dead-man safety timer that auto-restores passwords unless you
#     explicitly commit
#   · "Re-enable password login" is always available as break-glass
# =============================================================================

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

# ─── Paths ────────────────────────────────────────────────────────────────────
LOG_DIR="/opt/grin/logs"
LOG_FILE="$LOG_DIR/grin_ssh_hardening_$(date +%Y%m%d_%H%M%S).log"
CONF_DIR="/opt/grin/conf"
BACKUP_DIR="$CONF_DIR/ssh_backups"
SSHD_CONFIG="/etc/ssh/sshd_config"
DROPIN_DIR="/etc/ssh/sshd_config.d"
HARDENING_DROPIN="$DROPIN_DIR/99-grin-hardening.conf"
AUTH_KEYS="/root/.ssh/authorized_keys"
AUTOREVERT_PID="$CONF_DIR/ssh_autorevert.pid"
AUTOREVERT_SH="$CONF_DIR/ssh_autorevert.sh"
MARK_BEGIN="# >>> grin-toolkit ssh hardening >>>"
MARK_END="# <<< grin-toolkit ssh hardening <<<"
mkdir -p "$LOG_DIR" "$CONF_DIR" "$BACKUP_DIR"

# ─── Colors ───────────────────────────────────────────────────────────────────
RED='\033[0;31m'; GREEN='\033[0;32m'; YELLOW='\033[1;33m'
CYAN='\033[0;36m'; BOLD='\033[1m'; DIM='\033[2m'; RESET='\033[0m'

# ─── Logging ──────────────────────────────────────────────────────────────────
log()     { echo "[$(date -u '+%Y-%m-%d %H:%M:%S UTC')] $*" >> "$LOG_FILE" 2>/dev/null || true; }
info()    { echo -e "${CYAN}[INFO]${RESET}  $*"; log "[INFO]  $*"; }
success() { echo -e "${GREEN}[OK]${RESET}    $*"; log "[OK]    $*"; }
warn()    { echo -e "${YELLOW}[WARN]${RESET}  $*"; log "[WARN]  $*"; }
error()   { echo -e "${RED}[ERROR]${RESET} $*"; log "[ERROR] $*"; }

pause() { echo ""; echo "Press Enter to return to menu..."; read -r; }

confirm() {
    local prompt="$1"
    echo ""
    echo -ne "${BOLD}${YELLOW}▶ $prompt [y/N]: ${RESET}"
    local ans; read -r ans
    [[ "${ans,,}" == "y" ]]
}

# ─── Root guard ────────────────────────────────────────────────────────────────
if [[ ${EUID:-$(id -u)} -ne 0 ]]; then
    error "This tool must run as root (it edits /etc/ssh and /root/.ssh)."
    exit 1
fi

if ! command -v ssh-keygen &>/dev/null; then
    error "ssh-keygen not found — install the openssh client/server first."
    exit 1
fi

# =============================================================================
# SSH service helpers
# =============================================================================
_ssh_service_reload() {
    systemctl reload ssh   2>/dev/null && return 0
    systemctl reload sshd  2>/dev/null && return 0
    service ssh reload     2>/dev/null && return 0
    service sshd reload    2>/dev/null && return 0
    return 1
}

# Effective config value via `sshd -T` (lower-cased keyword), with a grep fallback.
_ssh_effective() {
    local key="$1"
    sshd -T 2>/dev/null | awk -v k="${key,,}" 'tolower($1)==k {print $2; exit}'
}

_ssh_port() {
    local p; p="$(_ssh_effective port)"
    echo "${p:-22}"
}

_ssh_dropin_supported() {
    [[ -d "$DROPIN_DIR" ]] && \
    grep -Eq '^[[:space:]]*Include[[:space:]]+/etc/ssh/sshd_config\.d/\*\.conf' "$SSHD_CONFIG"
}

_ssh_backup_config() {
    local ts dest
    ts="$(date +%Y%m%d_%H%M%S)"
    dest="$BACKUP_DIR/sshd_config.$ts.bak"
    cp -a "$SSHD_CONFIG" "$dest" 2>/dev/null && log "Backed up $SSHD_CONFIG -> $dest"
    echo "$dest"
}

_ssh_remove_managed_block() {
    [[ -f "$SSHD_CONFIG" ]] || return 0
    sed -i "/$MARK_BEGIN/,/$MARK_END/d" "$SSHD_CONFIG" 2>/dev/null || true
}

_ssh_remove_hardening() {
    rm -f "$HARDENING_DROPIN" 2>/dev/null || true
    _ssh_remove_managed_block
}

# =============================================================================
# Key installation (shared by all 3 add methods)
# Reads candidate public-key lines from stdin; validates + dedupes into
# /root/.ssh/authorized_keys.
# =============================================================================
_ssh_install_keys() {
    mkdir -p /root/.ssh
    chmod 700 /root/.ssh
    touch "$AUTH_KEYS"
    chmod 600 "$AUTH_KEYS"

    local installed=0 skipped=0 invalid=0 line tmp fp
    while IFS= read -r line; do
        line="$(printf '%s' "$line" | sed -e 's/^[[:space:]]*//' -e 's/[[:space:]]*$//')"
        [[ -z "$line" || "$line" == \#* ]] && continue

        tmp="$(mktemp)"
        printf '%s\n' "$line" > "$tmp"
        if fp="$(ssh-keygen -l -f "$tmp" 2>/dev/null)"; then
            if grep -qxF "$line" "$AUTH_KEYS"; then
                skipped=$((skipped + 1))
                info "Already present: $fp"
            else
                printf '%s\n' "$line" >> "$AUTH_KEYS"
                installed=$((installed + 1))
                success "Installed: $fp"
            fi
        else
            invalid=$((invalid + 1))
            warn "Not a valid public key, skipped: ${line:0:42}..."
        fi
        rm -f "$tmp"
    done

    echo ""
    info "Summary — installed: $installed, already-present: $skipped, invalid: $invalid"
    [[ $installed -gt 0 || $skipped -gt 0 ]]
}

# =============================================================================
# 1) Add key — paste manually
# =============================================================================
add_key_paste() {
    clear
    echo -e "${BOLD}${CYAN}  Add public key — paste manually${RESET}"
    echo ""
    echo -e "On your laptop the public key is usually in ${BOLD}~/.ssh/id_ed25519.pub${RESET}"
    echo -e "(or ${BOLD}id_rsa.pub${RESET}). Print it with: ${DIM}cat ~/.ssh/id_ed25519.pub${RESET}"
    echo ""
    echo -e "Paste one or more PUBLIC key lines below. Finish with an ${BOLD}empty line${RESET}:"
    echo ""

    local lines="" l
    while IFS= read -r l; do
        [[ -z "$l" ]] && break
        lines+="$l"$'\n'
    done

    if [[ -z "${lines//[$'\n\t ']/}" ]]; then
        warn "Nothing pasted — no changes made."
        pause; return 0
    fi
    printf '%s' "$lines" | _ssh_install_keys || true
    pause
}

# =============================================================================
# 2) Add key — generate on server
# =============================================================================
add_key_generate() {
    clear
    echo -e "${BOLD}${CYAN}  Add public key — generate on server${RESET}"
    echo ""
    echo -e "${YELLOW}Note:${RESET} the safest keys are generated on YOUR OWN machine and"
    echo -e "imported. Generate-on-server is a convenience — the private key is"
    echo -e "shown here ${BOLD}once${RESET}; you must copy it to your laptop now."
    echo ""
    if ! confirm "Generate a new ed25519 key pair on this server?"; then
        info "Cancelled."; pause; return 0
    fi

    local ts kf
    ts="$(date +%Y%m%d_%H%M%S)"
    kf="/root/.ssh/grin_ed25519_$ts"
    mkdir -p /root/.ssh; chmod 700 /root/.ssh
    if ! ssh-keygen -t ed25519 -N "" -C "grin-toolkit-$ts" -f "$kf" >/dev/null 2>&1; then
        error "ssh-keygen failed."; pause; return 0
    fi
    success "Key pair generated: $kf{,.pub}"

    # Install the public half into authorized_keys.
    _ssh_install_keys < "$kf.pub" || true

    echo ""
    echo -e "${BOLD}${RED}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${RESET}"
    echo -e "${BOLD}${RED}  PRIVATE KEY — shown ONCE. Save it to your laptop NOW.${RESET}"
    echo -e "${BOLD}${RED}  Store it at ~/.ssh/grin_ed25519 and: chmod 600 ~/.ssh/grin_ed25519${RESET}"
    echo -e "${BOLD}${RED}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${RESET}"
    echo ""
    cat "$kf"
    echo ""
    echo -e "${BOLD}Connect with:${RESET} ${DIM}ssh -i ~/.ssh/grin_ed25519 root@<server-ip> -p $(_ssh_port)${RESET}"
    echo ""

    if confirm "Have you saved the private key? Delete the server's private-key copy now (recommended)?"; then
        shred -u "$kf" 2>/dev/null || rm -f "$kf"
        success "Server private-key copy deleted ($kf). Public key stays in authorized_keys."
    else
        warn "Private key left on server at $kf — delete it once you've copied it."
    fi
    pause
}

# =============================================================================
# 3) Add key — fetch from URL (github username or raw URL)
# =============================================================================
add_key_url() {
    clear
    echo -e "${BOLD}${CYAN}  Add public key — fetch from URL${RESET}"
    echo ""
    echo -e "Enter a ${BOLD}GitHub username${RESET} (keys pulled from github.com/<user>.keys),"
    echo -e "or any ${BOLD}raw URL${RESET} that serves PUBLIC key lines"
    echo -e "(GitLab, or a self-hosted pubkey directory)."
    echo ""
    if ! command -v curl &>/dev/null; then
        error "curl not found — install curl or use paste instead."; pause; return 0
    fi
    echo -ne "${BOLD}GitHub username or URL: ${RESET}"
    local src url; read -r src
    src="$(printf '%s' "$src" | sed -e 's/^[[:space:]]*//' -e 's/[[:space:]]*$//')"
    if [[ -z "$src" ]]; then
        warn "Nothing entered."; pause; return 0
    fi
    if [[ "$src" == *"://"* ]]; then
        url="$src"
    else
        url="https://github.com/${src}.keys"
    fi
    info "Fetching $url ..."
    local body
    if ! body="$(curl -fsSL --max-time 20 "$url" 2>/dev/null)" || [[ -z "$body" ]]; then
        error "Could not fetch any key from $url"
        pause; return 0
    fi
    printf '%s\n' "$body" | _ssh_install_keys || true
    pause
}

